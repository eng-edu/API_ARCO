-- -----------------------------------------------------
CREATE SCHEMA IF NOT EXISTS `BD`;
USE `BD` ;

-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `BD`.`USUARIO` (
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
    `NOME` VARCHAR(245),
	`IDADE` VARCHAR(245),
    `SEXO` VARCHAR(245),
	`ESCOLARIDADE` VARCHAR(245),
	`EMAIL` VARCHAR(245),
	`SENHA` VARCHAR(245),
    `PONTO` VARCHAR(245),
    `CIDADE` VARCHAR(245),
    `BAIRRO` VARCHAR(245),
    `NUMERO` VARCHAR(245),
    `COMPLEMETO` VARCHAR(245),
    `CEP` VARCHAR(245),
    `TELEFONE` VARCHAR(245),
    `TIPO` VARCHAR(245),
    `PREMIUM` VARCHAR(245),
    `FOTO` VARCHAR(245),
  PRIMARY KEY (`ID`));

-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `BD`.`ARCO` (
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
    `ID_TEMATICA` INT(11),
	`TITULO` VARCHAR(245),
    `ID_LIDER` INT(11),
	`PONTO` VARCHAR(245),
	`CURTIDA` VARCHAR(245),
	`DESCURTIDA` VARCHAR(245),
    `DENUNCIA` VARCHAR(245),
	`SITUACAO` VARCHAR(245),
  PRIMARY KEY (`ID`));

-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `BD`.`TEMATICA` (
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
    `TITULO` VARCHAR(245),
	`DESCRICAO` VARCHAR(245),
  PRIMARY KEY (`ID`));

-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `BD`.`EQUIPE` (
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
    `ID_USUARIO` INT(11),
	`ID_ARCO` INT(11),
	`NOME` VARCHAR(245),
  PRIMARY KEY (`ID`));

-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `BD`.`ETAPA` (
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
    `CODIGO` INT(11),
	`TITULO` VARCHAR(245),
    `ID_ARCO` INT(11),
	`TEXTO` VARCHAR(245),
	`PONTO` VARCHAR(245),
	`SITUACAO` VARCHAR(245),
  PRIMARY KEY (`ID`));


CREATE TABLE IF NOT EXISTS `BD`.`ANUNCIO`(
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
    `TITULO` VARCHAR(245),
	`DESCRICAO` VARCHAR(245),
    `FOTO_ANUNCIO` VARCHAR(245),
    `LOGO_EMPRESA` VARCHAR(245),
    `EMPRESA` VARCHAR(245),
  PRIMARY KEY (`ID`));

CREATE TABLE IF NOT EXISTS `BD`.`PREMIO`(
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
    `TITULO` VARCHAR(245),
	`DESCRICAO` VARCHAR(245),
    `FOTO_PREMIO` VARCHAR(245),
	`LOGO_EMPRESA` VARCHAR(245),
    `EMPRESA` VARCHAR(245),
  PRIMARY KEY (`ID`));

CREATE TABLE IF NOT EXISTS `BD`.`MENSAGEM`(
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
    `ID_USUARIO` INT(11),
	`TEXTO` VARCHAR(245),
    `DATA` VARCHAR(245),
	`HORA` VARCHAR(245),
    `ID_ARCO` INT(11),
  PRIMARY KEY (`ID`));

CREATE TABLE IF NOT EXISTS `BD`.`INFO_ARCO`(
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
    `ID_USUARIO` INT(11),
    `ID_ARCO` INT(11),
	`TIPO` VARCHAR(245),
  PRIMARY KEY (`ID`));

